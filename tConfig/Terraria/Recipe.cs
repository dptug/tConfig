							break;
						}
					}
					continue;
				}
				for (int l = 0; l < maxRequirements && Main.recipe[j].requiredItem[l].type != 0; l++)
				{
					int num3 = Main.recipe[j].requiredItem[l].stack;
					for (int m = 0; m < 48; m++)
					{
						if (Main.player[Main.myPlayer].inventory[m].IsTheSameAs(Main.recipe[j].requiredItem[l]))
						{
							num3 -= Main.player[Main.myPlayer].inventory[m].stack;
						}
						if (num3 <= 0)
						{
							break;
						}
					}
					if (num3 > 0)
					{
						flag = false;
						break;
					}
				}
				if (!flag)
				{
					continue;
				}
				bool flag3 = true;
				for (int n = 0; n < maxRequirements && Main.recipe[j].requiredTile[n] != -1; n++)
				{
					if (!Main.player[Main.myPlayer].adjTile[Main.recipe[j].requiredTile[n]])
					{
						flag3 = false;
						break;
					}
				}
				if (flag3 && (!Main.recipe[j].needWater || Main.player[Main.myPlayer].adjWater) && (!Main.recipe[j].needLava || Main.player[Main.myPlayer].adjLava))
				{
					Main.availableRecipe[Main.numAvailableRecipes] = j;
					Main.numAvailableRecipes++;
				}
			}
			for (int num4 = 0; num4 < Main.numAvailableRecipes; num4++)
			{
				if (num == Main.availableRecipe[num4])
				{
					Main.focusRecipe = num4;
					break;
				}
			}
			if (Main.focusRecipe >= Main.numAvailableRecipes)
			{
				Main.focusRecipe = Main.numAvailableRecipes - 1;
			}
			if (Main.focusRecipe < 0)
			{
				Main.focusRecipe = 0;
			}
			float num5 = Main.availableRecipeY[Main.focusRecipe] - num2;
			for (int num6 = 0; num6 < maxRecipes; num6++)
			{
				Main.availableRecipeY[num6] -= num5;
			}
			Codable.RunGlobalMethod("ModGeneric", "OnRecipeRefresh");
		}

