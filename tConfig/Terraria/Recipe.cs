					for (int k = 0; k < maxRequirements && Main.recipe[j].requiredItem[k].type != 0; k++)
					{
						if (Main.guideItem.IsTheSameAs(Main.recipe[j].requiredItem[k]))
						{
							Main.availableRecipe[Main.numAvailableRecipes] = j;
