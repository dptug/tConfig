		public static string AddSimpleRecipe(object output, params object[] args)
		{
			if (output is Item)
			{
				newRecipe.createItem.SetDefaults(((Item)output).name);
				newRecipe.createItem.stack = ((Item)output).stack;
			}
			else if (output is string)
			{
				string text = (string)output;
				newRecipe.createItem.SetDefaults(text);
				if (newRecipe.createItem.name == "Unloaded Item")
				{
					string s = text.Split(' ')[0];
					if (!int.TryParse(s, out int result))
					{
						newRecipe = new Recipe();
						throw new Exception("Bad argument for Recipe.addSimpleRecipe(): " + text);
					}
					newRecipe.createItem.SetDefaults(text.Substring(text.IndexOf(' ') + 1));
					if (newRecipe.createItem.name == "Unloaded Item")
					{
						newRecipe = new Recipe();
						throw new Exception("Bad argument for Recipe.addSimpleRecipe(): " + text);
					}
					newRecipe.createItem.stack = result;
				}
			}
			int num = 0;
			int num2 = 0;
			for (int i = 0; i < args.Length; i++)
			{
				if (args[i] is Item)
				{
					newRecipe.requiredItem[num++] = ((Item)args[i]).CloneItem();
					continue;
				}
				if (args[i] is int)
				{
					newRecipe.requiredTile[num2++] = (int)args[i];
					continue;
				}
				if (args[i] is string)
				{
					string text2 = (string)args[i];
					if (text2 == "@Water")
					{
						newRecipe.needWater = true;
						continue;
					}
					if (text2 == "@Lava")
					{
						newRecipe.needLava = true;
						continue;
					}
					if (text2.StartsWith("@"))
					{
						newRecipe.requiredTile[num2++] = Config.tileDefs.ID[text2.Substring(1)];
						continue;
					}
					newRecipe.requiredItem[num].SetDefaults(text2);
					if (newRecipe.requiredItem[num].name == "Unloaded Item")
					{
						string s2 = text2.Split(' ')[0];
						if (!int.TryParse(s2, out int result2))
						{
							newRecipe = new Recipe();
							throw new Exception("Bad argument for Recipe.addSimpleRecipe(): " + text2);
						}
						newRecipe.requiredItem[num].SetDefaults(text2.Substring(text2.IndexOf(' ') + 1));
						if (newRecipe.requiredItem[num].name == "Unloaded Item")
						{
							newRecipe = new Recipe();
							throw new Exception("Bad argument for Recipe.addSimpleRecipe(): " + text2);
						}
						newRecipe.requiredItem[num].stack = result2;
					}
					num++;
					continue;
				}
				newRecipe = new Recipe();
				throw new Exception("Bad argument type for Recipe.addSimpleRecipe(): " + args[i].GetType().Name);
			}
			Array.Resize(ref Main.recipe, Main.recipe.Length + 1);
			addRecipe();
			for (int j = 0; Main.recipe[numRecipes - 1].requiredItem[j].type > 0; j++)
			{
				Main.recipe[numRecipes - 1].requiredItem[j].checkMat();
			}
			Main.recipe[numRecipes - 1].createItem.checkMat();
			string text3 = "tConfigSimpleRecipe" + nextSimpleRecipe++;
			Config.recipeByName[text3] = Main.recipe[numRecipes - 1];
			return text3;
		}
